import { Link } from "react-router-dom";
import { useAuth } from "../../contexts/AuthContext";
import "./Logo.scss";

const Logo = () => {
  const { isAuthenticated, role } = useAuth();

  const getTargetUrl = () => {
    if (!isAuthenticated || !role) return "/";

    const dashboardRole = role.toLowerCase();
    switch (dashboardRole) {
      case "admin":
        return "/admin-dashboard";
      case "manager":
        return "/manager-dashboard";
      case "staff":
        return "/staff-dashboard";
      case "client":
        return "/client-dashboard";
      case "vendor":
        return "/vendor-dashboard";
      default:
        return "/";
    }
  };

  return (
    <>
      <Link
        to={getTargetUrl()}
        className="brand-logo-link"
        onClick={() => window.scrollTo({ top: 0, behavior: "smooth" })}
      >
        <svg
          viewBox="0 0 240 65"
          className="brand-logo"
          preserveAspectRatio="xMinYMid meet"
        >
          <defs>
            <linearGradient
              id="logoGradient"
              x1="0%"
              y1="0%"
              x2="100%"
              y2="100%"
            >
              <stop
                offset="0%"
                style={{ stopColor: "#7c3aed", stopOpacity: 1 }}
              />
              <stop
                offset="100%"
                style={{ stopColor: "#a855f7", stopOpacity: 1 }}
              />
            </linearGradient>
          </defs>

          {/* Floral Element - Increased Size */}
          <g transform="translate(20, 32)">
            <circle cx="0" cy="0" r="4" fill="url(#logoGradient)" />
            <ellipse
              cx="-7"
              cy="-2.5"
              rx="5"
              ry="7.5"
              fill="url(#logoGradient)"
              opacity="0.4"
              transform="rotate(-30, -7, -2.5)"
            />
            <ellipse
              cx="7"
              cy="-2.5"
              rx="5"
              ry="7.5"
              fill="url(#logoGradient)"
              opacity="0.4"
              transform="rotate(30, 7, -2.5)"
            />
            <ellipse
              cx="-5.5"
              cy="5.5"
              rx="5"
              ry="7"
              fill="url(#logoGradient)"
              opacity="0.4"
              transform="rotate(-60, -5.5, 5.5)"
            />
            <ellipse
              cx="5.5"
              cy="5.5"
              rx="5"
              ry="7"
              fill="url(#logoGradient)"
              opacity="0.4"
              transform="rotate(60, 5.5, 5.5)"
            />
          </g>

          {/* Main Text */}
          <text
            x="42"
            y="38"
            fontFamily="'Fredoka One', 'Comic Sans MS', cursive"
            fontSize="32"
            fontWeight="400"
            fill="url(#logoGradient)"
            letterSpacing="0"
          >
            WeddsPot
          </text>

          {/* Tagline */}
          <text
            x="44"
            y="52"
            fontFamily="'Inter', sans-serif"
            fontSize="9"
            fill="#666"
            letterSpacing="2.5"
            fontWeight="400"
          >
            where dreams come true
          </text>
        </svg>
      </Link>
    </>
  );
};

export default Logo;
